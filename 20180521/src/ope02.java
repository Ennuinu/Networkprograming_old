
public class ope02 {

	public static void main(String[] args) {
		int i =10;
		i = i + 2;
		System.out.println(i);
		i += 2;
		System.out.println(i);		// i = i + 2 と同じ
		i++;
		System.out.println(i);		// インクリメント
		i--;
		System.out.println(i);		// デクリメント
		i -=2;
		System.out.println(i);
		i = i - 2;
		System.out.println(i);		// i -= 2 と同じ

	}

}
