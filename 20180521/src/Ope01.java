
public class Ope01 {

	public static void main(String[] args) {
		int a = 14;
		int b = 4;
		System.out.println("a+b=" + (a + b));		//加算
		System.out.println("a-b=" + (a - b));		//減算
		System.out.println("a*b=" + (a * b));		//乗算
		System.out.println("a/b=" + (a / b));		//除算 結果は整数、小数点以下切り捨て
		System.out.println("a%b=" + (a % b));		//剰余

	}

}
